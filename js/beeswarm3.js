var Alldata = data.map(d => ({ data: d, index: d.id })).sort((a, b) => a.index - b.index);

function compute(){
    var xScale = d3.scaleLinear()
        .domain(d3.extent(data, d => d.year))
        .range([margin.top, height]);
    var yScale = d3.scaleLinear()
        .domain(d3.extent(data, d => d.year))
        .range([margin.left, width - margin.right]);

    //一.compute the simple position 
    const simcir0 = data.map(d => ({ m: yScale(d.year), data: d, index: d.id })).sort((a, b) => a.index - b.index);
    const simcir = data.map(d => ({ m: xScale(d.year), data: d, index: d.id })).sort((a, b) => a.index - b.index);

    simpos(simcir0, height);

    for (c = 0; c < simcir0.length; c++) {
        const x = simcir0[c].m;
        const y = simcir0[c].n;
        const i = simcir0[c].index;

        for (d = 0; d < Alldata.length; d++) {
            if (Alldata[d].index === i) {
                Alldata[d].x1 = x;
                Alldata[d].y1 = y;
            }
        }
    };

    //二.compute t&a position data
    tencir = simcir.filter(function (d) { return d.data.inname === "腾讯" });
    alicir = simcir.filter(function (d) { return d.data.inname === "阿里" });

    simpos(tencir, width /2);
    simpos(alicir, (width - halfSpace) / 2);

    for (c = 0; c < tencir.length; c++) {
        const x = tencir[c].n;
        const y = tencir[c].m;
        const i = tencir[c].index;

        for (d = 0; d < Alldata.length; d++) {
            if (Alldata[d].index === i) {
                Alldata[d].x2 = x;
                Alldata[d].y2 = y;
            }
        }
    };

    for (c = 0; c < alicir.length; c++) {
        const x = alicir[c].n;
        const y = alicir[c].m;
        const i = alicir[c].index;

        for (d = 0; d < Alldata.length; d++) {
            if (Alldata[d].index === i) {
                Alldata[d].x2 = x;
                Alldata[d].y2 = y;
            }
        }
    };
    //三.compute type position data
    liType = ["文化娱乐", "电子商务", "企业服务", "汽车交通", "本地生活", "金融", "游戏", "医疗健康", "工具软件", "教育", "物流", "社交网络", "硬件", "旅游", "房产服务", "体育运动", "广告营销"]
    for (t = 0; t < liType.length; t++){
        //1.filter data by type
        var sdata = data.filter(function (d) { return d.type === liType[t]});

        //2.compute every data's (x,y) by pack method
        var circles = d3.packSiblings(sdata
            .map(function(d){ return {data: d, index: d.id, r: radius + Tpadding};}));
        // console.log("c:");
        // console.log(circles);

        //3.translate the meta data
        var ncircles = circles.map(d => cpost(d, i));
        // console.log(ncircles);

        //4.match tdata to the Alldata 
        for (c = 0; c < ncircles.length; c++) {
            const x = ncircles[c].x;
            const y = ncircles[c].y;
            const id = ncircles[c].index;

            for (d = 0; d < Alldata.length; d++) {
                if (Alldata[d].index === id) {
                    Alldata[d].x3 = x;
                    Alldata[d].y3= y;
                }
            }
        };
        //5.end and recircle
        i = i+1;
        // console.log(i);

        function cpost(d, i){
            // console.log(i, tnum);
            if( i<tnum ){
                d.x = d.x + i * Ppadding
            }
            else if( i >= tnum && i < tnum*2){
                d.x = d.x + (i-tnum) * Ppadding;
                d.y = d.y + Ppadding2 *1.1
            } else if (i >= tnum * 2 && i < tnum * 3) {
                d.x = d.x + (i - tnum*2) * Ppadding;
                d.y = d.y + Ppadding2 * 1.9
            }
            else if (i >= tnum * 3 && i < tnum * 4) {
                d.x = d.x + (i - tnum * 3) * Ppadding;
                d.y = d.y + Ppadding2 * 2.6
            }
            return d;
        };
        
    };
    //四.compute type&time position data 
    liType2 = ['','',''];
    
    //common function
    function simpos(data, edge) {
        const epsilon = 1e-3;
        let head = null, tail = null;
        // Place each circle sequentially.
        for (const b of data) {

            // Remove data from the queue that can’t intersect the new circle b.
            while (head && head.m < b.m - (radius * 2 + padding)) head = head.next;

            // Choose the minimum non-intersecting tangent.
            if (intersects(b.m, b.n = 0)) {
                let a = head;
                b.n = Infinity;
                do {
                    let n = a.n + Math.sqrt((radius * 2 + padding) ** 2 - (a.m - b.m) ** 2);
                    if (n < b.n && !intersects(b.m, n)) b.n = n;
                    
                    a = a.next;
                } while (a);
            };
            // Add b to the queue.
            b.next = null;
            if (head === null) head = tail = b;
            else tail = tail.next = b;
        }
        // Returns true if circle ⟨m,n⟩ intersects with any circle in the queue.
        function intersects(m, n) {
            let a = head;
            while (a) {
                if ((radius * 2 + padding - epsilon) ** 2 > (a.m - m) ** 2 + (a.n - n) ** 2) {
                    return true;
                }
                a = a.next;
            }
            return false;
        }
        for (b of data){
            if (b.n > edge && b.n < edge * 2) {
                b.m = b.m + radius * 2 + padding;
                b.n = b.n - edge;
            } else if (b.n > edge*2 && b.n < edge*3){
                b.m = b.m + radius * 4 + padding*2;
                b.n = b.n - edge * 2 ;
            } else if (b.n > edge * 3 && b.n < edge * 4) {
                b.m = b.m + radius * 6 + padding * 3;
                b.n = b.n - edge * 3;
            } else if (b.n > edge * 4 && b.n < edge * 5) {
                b.m = b.m + radius * 8 + padding * 4;
                b.n = b.n - edge * 4;
            }  
        }
        return data;
    };  
};


